stack_list = []

#Stack consists of two operation
# Push & Pop 
# It is called LIFO =  Last in Fast out

# Now lets push some element in the list
stack_list.append('injam')
stack_list.append('najmus')
stack_list.append('c')
stack_list.append(1)
print("Print result before any pop operation: ")
print(stack_list)

# Now lets do pop operation
stack_list.pop()
stack_list.pop()
print("Print result after any pop operation: ")
print(stack_list)

